def add_one(some_list):
    # Перетворюємо список цифр у число
    number = int("".join(map(str, some_list)))

    # Додаємо 1
    number += 1

    # Розбиваємо число на окремі цифри та повертаємо у вигляді списку
    return [int(digit) for digit in str(number)]


# Тестування
assert add_one([1, 2, 3, 4]) == [1, 2, 3, 5], "Test1"
assert add_one([9, 9, 9]) == [1, 0, 0, 0], "Test2"
assert add_one([0]) == [1], "Test3"
assert add_one([9]) == [1, 0], "Test4"

print("ОК")  # Якщо всі тести проходять, то код працює правильно
pass
# - map(str, some_list) — Перетворює список цифр у список рядків.
# - "".join(...) — Об'єднує всі цифри у один рядок, який представляє число.
# - int(...) — Перетворює рядок на ціле число.
# - Додаємо 1 до отриманого числа.
# - str(number) — Перетворюємо число назад у рядок, щоб розбити його на окремі символи.
# - Список [int(digit) for digit in str(number)] — кожен символ (цифру) перетворюємо у int та формуємо список.
